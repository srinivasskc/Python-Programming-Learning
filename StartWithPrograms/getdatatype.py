#Getting Data Type
x=98483 #int character
print(type(x))

y=1000000000000000000000000000000000000000000000000000.0  #float characters
print(type(y))

text = "Hello Good Morning"  # string text
print(type(text))

userAge = [21,22,23,24]
print(type(userAge))

userAge1 = ["Jan","Feb",7, 21]  #list of values -- list
print(type(userAge1))

monthOfYear = ("Jan","Feb")  # constant list of values -- tuple
print(type(monthOfYear))

#set of data
map1 = {"Birthday: 1 ; Marriage Day: 2 ; Dream Day : 3"}
print(type(map1))


dictMap = {"subj1" : "Maths"} # pair of data - key: value
print(type(dictMap))

dictMap2 = {"Peter" : 55, "sanju" : 59}
print(type(dictMap2))

dictMap3 = dict(peter = 37, sanju = 25)
print(type(dictMap3))

dictMap4 = {"One" : 1 , 2.5: "Two Point Five", 'Three' : 'Thirty Three'}
print(type(dictMap4))

