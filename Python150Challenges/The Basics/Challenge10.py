# 010  There are 2,204 pounds in a kilogram. Ask the user to enter a weight in kilograms and convert it to pounds.

kilogram_in_pounds = 2200
weight_in_kilograms = float(input("Enter the Weight: "))
weight_in_pounds = weight_in_kilograms * kilogram_in_pounds
print("Weight in Pounds: ", weight_in_pounds)
