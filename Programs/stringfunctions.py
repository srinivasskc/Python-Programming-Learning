# Functions in Strings

# Upper() - converts the string to upper case
str1 = "Online Learning"
print(str1.upper())
print(str1.lower())

# Is Upper() - Will display True/False. True when string is in Upper Case.
print(str1.isupper())

# Length() - Prints length of the string
print(len(str1))

# With Index - Get character from string.
print(str1[0])
print(str1[14])

# Replace: ("oldtext", "newtext") - old string with new string.
print(str1.replace("Online", "Great"))


