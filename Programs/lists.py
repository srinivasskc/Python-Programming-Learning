# Lists in Python
# Bunch of data values

friends = ["Varun", "Kumar", "Kiran"]
print(friends)

# Print Specific Element from List
print(friends[0])
print(friends[1])
print(friends[2])

# Access elements from last
print(friends[-1])
print(friends[-2])

# Access elements from 1st index till last
print(friends[1:])
friends = ["Varun", "Kumar", "Kiran", "Kailash", "Abhilash"]
print(friends[1:4])

# Modifying the friends value.
friends[1] = "Ankita"
print(friends)
