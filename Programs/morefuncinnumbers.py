# More Functions in Numbers

from math import *

# Floor Function - Grabs lowest number, ignores the decimal number
print(floor(3.7))
print(floor(4.0))

# Ceil Function - Fetches the next number, ignores the decimal number
print(ceil(3.7))
print(ceil(3.4))
print(ceil(3.5))
print(ceil(3.1))
print(ceil(3.0))   # Except for this. It returns 3

# sqrt Function.
print(sqrt(4))

